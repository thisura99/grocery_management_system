    private void catsearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_catsearchKeyReleased
        // TODO add your handling code here:
        // TODO add your handling code here:
        String name=catsearch.getText();
        try {
            DefaultTableModel dt=(DefaultTableModel)categorytable.getModel();
            dt.setRowCount(0);
            Statement sta=DatabaseConnection.myconnection().createStatement();
            ResultSet res = sta.executeQuery("SELECT * FROM `productcategory` WHERE Category_name like'%"+name+"%'");
            categorytable.setModel(DbUtils.resultSetToTableModel(res));
        } catch (Exception e) {
            tableload();
        }
    }//GEN-LAST:event_catsearchKeyReleased


