    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // Data Save:
        String id=cataid.getText();
        String name=cataname.getText();
        String description=catadescription.getText();
        String date=catadate.getText();
        if(name.isEmpty()||id.isEmpty()||name.isEmpty()){
            JOptionPane.showMessageDialog(null,"Some Component Are Missing");
        }
        else{
            try {
                Statement st=DatabaseConnection.myconnection().createStatement();
                st.executeUpdate("INSERT INTO `productcategory`(`Category_id`, `Category_name`, `Categorry_description`, `Category_date`) VALUES ('"+id+"','"+name+"','"+description+"','"+date+"')");
                JOptionPane.showMessageDialog(null,"Data Saved Successfully");
            } catch (Exception e) {
                System.out.println(e);
            }
            tableload();
            clearfield();
        }
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // Update Data:
        String id=cataid.getText();
        String name=cataname.getText();
        String description=catadescription.getText();
        String date=catadate.getText();
        
        if(cataid.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No Record Are Selected");
        }
        else{
            try {
                Statement st=DatabaseConnection.myconnection().createStatement();
                st.executeUpdate("UPDATE `productcategory` SET `Category_id`='"+id+"',`Category_name`='"+name+"',`Categorry_description`='"+description+"',`Category_date`='"+date+"' WHERE `Category_id`='"+id+"'");
                
                JOptionPane.showMessageDialog(null,"Data Update Successfully");
            } catch (Exception e) {
            }
            tableload();
            date();
            clearfield();
            
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // Delete Data:
        if(cataid.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No Record Are Selected");
        }
        else{
            int response = JOptionPane.showConfirmDialog(this,"Do You Want To Delete This Record","Confrim",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);


            if(response==JOptionPane.YES_OPTION){
                String id=cataid.getText();
                try {
                    Statement st=DatabaseConnection.myconnection().createStatement();
                    st.executeUpdate("DELETE FROM `productcategory` WHERE   Category_id='"+id+"'");
                    JOptionPane.showMessageDialog(null,"Data Delete Successfully");
                    
                } catch (Exception e) {
                    System.out.println(e);
                }
                tableload();
                clearfield();
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

