    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // Search Data:
        
        String id=cataid.getText();
        
        if(cataid.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter Category ID First!");
        }
        else{
            try {

                Statement st=DatabaseConnection.myconnection().createStatement();
                ResultSet rs=st.executeQuery("SELECT * FROM `productcategory` WHERE Category_id='"+id+"'");

                if(rs.next()){
                    cataname.setText(rs.getString("Category_name"));
                    catadescription.setText(rs.getString("Categorry_description"));
                    catadate.setText(rs.getString("Category_date"));
                }
                else{
                    JOptionPane.showMessageDialog(null, "Data Is Not In Database, Try Again!");
                }

            } catch (Exception e) {
                System.out.println(e);
            }
        } 
    }//GEN-LAST:event_btnsearchActionPerformed

